int		ft_sqrt(int n)
{
	int	i;
	int	result;

	i = 0;
	while (i <= n / 2)
	{
		if (i * i == n)
			return (i);
		if (i * i < n)
			result = i;
		i++;
	}
	return (result + 1);
}
