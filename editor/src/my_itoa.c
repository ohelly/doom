#include "doom_editor.h"

void		my_itoa(char *str, int num)
{
	if (num < 10)
	{
		str[0] = '0';
		str[1] = '0';
		str[2] = '0' + num;
	}
	else if (num < 100)
	{
		str[2] = '0' + num % 10;
		str[1] = '0' + num / 10;
		str[0] = '0';
	}
	else if (num < 1000)
	{
		str[2] = '0' + num % 10;
		num = num / 10;
		str[1] = '0' + num % 10;
		str[0] = '0' + num / 10;
	}
}
